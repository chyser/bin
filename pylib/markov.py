class BiGram(BaseMarkov):
#-------------------------------------------------------------------------------
    START = 26
    END = 26
    ISIZE = 27
    SIZE = 27
    CHARSIZE = 1

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def __str__(self):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        s = ['# a   b   c   d   e   f   g   h   i   j   k   l   m   n   o   p   q   r   s   t   u   v   w   x   y   z  END']
        for i in range(self.SIZE):
            t = '[' + ','.join(['%03d' % ch for ch in self.hist[i]]) + ']'
            lt = len(t)
            tt = 109 - lt
            s.append(t + (' ' * (2 + tt) + '# %s - %d' % (self.mkChar(i), i)))

        return '\n'.join(s)

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def mkIdx(self, a, b):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return b


