class TriGram(BaseMarkov):
#-------------------------------------------------------------------------------
    START = 26
    END = 26
    ISIZE = 27
    SIZE = 27*27
    CHARSIZE = 2

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def add(self, wd):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        if not wd.isalpha():
            return

        wl = ['START'] + list(wd) + ['END']
        for i in range(len(wl) - self.CHARSIZE):
            self.hist[self.cvt(wl[i:i+self.CHARSIZE])][BaseMarkov.cvt(self, wl[i+self.CHARSIZE])] += 1

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def cvt(self, ch):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return BaseMarkov.cvt(self, ch[0]) * self.ISIZE + BaseMarkov.cvt(self, ch[1])

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def getStart(self):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        choices = []

        s = self.START * self.ISIZE
        for i in range(27):
            idx = s + i
            if sum(self.hist[idx]) > 0:
                choices.append((idx, i))

        return self.rg.choice(choices)

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def mkIdx(self, a, b):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return a * self.ISIZE + b

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def __str__(self):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        s = ['# a   b   c   d   e   f   g   h   i   j   k   l   m   n   o   p   q   r   s   t   u   v   w   x   y   z  END']
        for i in range(self.SIZE):
            s.append('[' + ','.join(['%03d' % ch for ch in self.hist[i]]) + ']')
        return '\n'.join(s)



