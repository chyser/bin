class MarkovWord(object):
#-------------------------------------------------------------------------------
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def __init__(self, seed=None, db=None, cls=BiGram, rg=None):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        self.rg = random.Random() if rg is None else rg
        self.rg.seed(seed if seed is not None else os.urandom(4))
        self.mk = cls(db, self.rg)

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def gen(self, maxSize, start=None):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        idx, cidx = self.mk.getStart() if start is None else start
        wd = [] if cidx is None else [self.mk.mkChar(cidx)]

        while maxSize > 0:
            ## build totals as we go
            if self.mk.sums[idx] is None:
                self.mk.sums[idx] = sum(self.mk.hist[idx])

            rowTotal = self.mk.sums[idx]
            assert rowTotal > 0

            ## get a random number between 1 and the total samples in a row
            r = self.rg.randint(1, rowTotal)

            ## walk row adding each sample until we get a sum greater than than number chosen
            t = 0
            for i in range(27):
                t += self.mk.hist[idx][i]

                if r <= t:
                    ## if last entry, 'END', bail out
                    if i == self.mk.END:
                        return ''.join(wd)

                    ## get next entry
                    idx = self.mk.mkIdx(cidx, i)
                    cidx = i

                    ## add charachter to word
                    wd.append(self.mk.mkChar(i))
                    maxSize -= 1
                    break

        return ''.join(wd)

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def genWord(self, size, start=None, capitalize=True):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        wd = ""
        while len(wd) < size:
            lt = size - len(wd)
            if lt < 3:
                lt = 3
            elif lt > 8:
                lt = 8
            twd = self.gen(lt, start)

            if not self.wordCheck(twd):
                continue

            wd += twd.capitalize() if capitalize else twd
        return wd

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def word(self, maxSize, start=None):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        """ get a word no larger than 'maxSize' starting with letter 'start'
        """
        while 1:
            wd = self.gen(maxSize, start)
            if self.wordCheck(wd):
                return wd.capitalize()

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def wordCheck(self, wd):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        if len(wd) < 3:
            return

        hasVowel = False

        lch = '';  cnt = ccnt = vcnt = 0
        for idx, ch in enumerate(wd):
            if lch == ch:
                cnt += 1

                if cnt > 2:
                    return

                if cnt == 1 and idx == 1:
                    return
            else:
                cnt = 0

            lch = ch

            if ch in set(list('aeiouy')):
                hasVowel = True
                ccnt = 0
                vcnt += 1
            else:
                ccnt += 1
                vcnt = 0

            if ccnt > 2 or vcnt > 2:
                return

        return hasVowel

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def saveString(self):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return self.mk.saveString()

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def loadString(self, s):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return self.mk.loadString(s)

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def learnString(self, s):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return self.mk.learnString(s)

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def learnFile(self, fileName):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return self.mk.learnFile(fileName)

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def __str__(self):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return str(self.mk)


