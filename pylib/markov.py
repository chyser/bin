class BaseMarkov(object):
#-------------------------------------------------------------------------------
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def __init__(self, db=None, rg=None):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        object.__init__(self)
        self.hist = [[0 for i in range(self.ISIZE)] for j in range(self.SIZE)]
        self.sums = [None for i in range(self.SIZE)]

        if db is not None:
            self.loadString(db)

        self.rg = random.random() if rg is None else rg

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def getStart(self):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return self.START, None

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def add(self, wd):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        """ add a word to the table
        """
        if not wd.isalpha():
            return

        wl = ['START'] + list(wd) + ['END']
        for i in range(len(wl) - self.CHARSIZE):
            self.hist[self.cvt(wl[i])][self.cvt(wl[i+self.CHARSIZE])] += 1

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def cvt(self, ch):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        if ch in set(['END', 'START']):
            return self.START

        return ord(ch.upper()) - ord('A')

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def mkChar(self, num):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        if num == self.START: return 'START'
        return chr(num + ord('A')).lower()

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def saveString(self):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return '\n'.join([str(self.hist[i]) for i in range(self.SIZE)])

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def loadString(self, s):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        """ load the table. assume each line is a python array when 'eval'ed.
        """
        self.hist = []
        for i, line in enumerate(s.split('\n')):
            if not line or line[0] == '#': continue

            ary = eval(line)

            ## if short, then pad
            if len(ary) != self.SIZE:
                ary.append(0)
            self.hist.append(ary)

        if i != self.SIZE:
            self.hist.append([1]*self.SIZE)

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def learnString(self, s):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        for wd in self.splitWords(s):
            self.add(wd)
        return self.saveString()

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def learnFile(self, fileName):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        with open(fileName, 'rU') as inf:
            for line in inf:
                self.learnString(line)
        return self.saveString()

    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    def splitWords(self, s):
    #- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        return s.split()


